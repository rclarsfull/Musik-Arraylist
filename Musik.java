public class Musik{
  
  public String name,interpret;
  public double laengeMin;
  
  Musik(){};
  Musik(String name, String interpret,double laengeMin){
    this.name=name;
    this.interpret=interpret;
    this.laengeMin=laengeMin;
    }
  
  public String toString(){
    return this.name+" von "+this.interpret+" dauret "+laengeMin+" Minuten.";
  }
  
  
  }
